import os
from fctlib import get_case_path

#################################################################################
# CONSTANTS
#################################################################################


#################################################################################
# MAIN FUNCTION
#################################################################################
def remove(PATH, case_name):
    '''
    DEF:    remove case.
    INPUT:  - case_name: name of the case, type=string
    OUTPUT: - ()
    '''
    case_path = get_case_path(PATH, case_name)
    os.system('rm -r ' + str(os.path.join(case_path)))
